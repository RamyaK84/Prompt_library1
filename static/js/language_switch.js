let currentLang = localStorage.getItem('promptlib_lang') || 'en';

function applyTranslations(lang) {
  currentLang = lang;
  localStorage.setItem('promptlib_lang', lang);

  // Update all data-i18n text elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });

  // Update lang badge in button
  const badge = document.getElementById('currentLang');
  if (badge) badge.textContent = lang === 'en' ? 'EN' : 'த';

  // Update active state on dropdown options
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.toggle('active', opt.dataset.lang === lang);
  });

  // Update html lang attribute
  document.documentElement.lang = lang === 'ta' ? 'ta' : 'en';
}

document.addEventListener('DOMContentLoaded', function () {
  const langBtn = document.getElementById('langBtn');
  const langDropdown = document.getElementById('langDropdown');

  if (langBtn && langDropdown) {
    langBtn.addEventListener('click', function (e) {
      e.stopPropagation();
      langDropdown.classList.toggle('open');
    });
    document.addEventListener('click', function () {
      langDropdown.classList.remove('open');
    });
    document.querySelectorAll('.lang-option').forEach(opt => {
      opt.addEventListener('click', function (e) {
        e.stopPropagation();
        applyTranslations(this.dataset.lang);
        langDropdown.classList.remove('open');
      });
    });
  }

  // Apply saved language on every page load
  applyTranslations(currentLang);
});
